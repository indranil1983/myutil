package com.ericsson.spring.model;

public class FlowDocBeanChat 
{
	private String event;
	private String content;
	private String tags;
	private String external_user_name;
	private String subject;
	private String from_address;
	private String source;
	
	
	public String getEvent() {
		return event;
	}
	public void setEvent(String event) {
		this.event = event;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getTags() {
		return tags;
	}
	public void setTags(String tags) {
		this.tags = tags;
	}
	public String getExternal_user_name() {
		return external_user_name;
	}
	public void setExternal_user_name(String external_user_name) {
		this.external_user_name = external_user_name;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getFrom_address() {
		return from_address;
	}
	public void setFrom_address(String from_address) {
		this.from_address = from_address;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	
	
}
